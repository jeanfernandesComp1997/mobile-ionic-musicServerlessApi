export class Music {

    name: string;
    artist: string;
    date: string;
    link: string;
    image: string;

    constructor(name: string, artist: string, date: string, link: string, image: string) {

        this.name = name;
        this.artist = artist;
        this.date = date;
        this.link = link;
        this.image = image;
    }
}